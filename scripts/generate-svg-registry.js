#!/usr/bin/env node
/**
 * Reads svg/*.svg and generates src/icons-svg.generated.ts with a record of iconKey -> SVG string.
 * Run after export-svgs-from-figma.js to embed SVGs in the package.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const SVG_DIR = path.join(__dirname, '../svg');
const OUT_FILE = path.join(__dirname, '../src/icons-svg.generated.ts');

if (!fs.existsSync(SVG_DIR)) {
  fs.mkdirSync(SVG_DIR, { recursive: true });
}

const files = fs.readdirSync(SVG_DIR).filter((f) => f.endsWith('.svg'));
const entries = [];
for (const f of files) {
  const iconKey = f.replace(/\.svg$/, '');
  const content = fs.readFileSync(path.join(SVG_DIR, f), 'utf8');
  const escaped = content
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$/g, '\\$');
  entries.push(`  "${iconKey}": \`${escaped}\``);
}

const ts = `/** Auto-generated by scripts/generate-svg-registry.js - do not edit */\n\nexport const svgContent: Record<string, string> = {\n${entries.join(',\n')}\n};\n`;
fs.writeFileSync(OUT_FILE, ts);
console.log('Wrote', OUT_FILE, `(${entries.length} icons)`);
